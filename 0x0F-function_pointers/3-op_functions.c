# include "3-calc.h"

/**
 * op_add - sum operation.
 *
 * @a: first argument.
 * @b: second argument.
 *
 * Return: the result.
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substraction operation.
 *
 * @a: first argument.
 * @b: second argument.
 *
 * Return: the result.
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication operation.
 *
 * @a: first argument.
 * @b: second argument.
 *
 * Return: the result.
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - the division operation.
 *
 * @a: The first argument
 * @b: The second argument.
 *
 * Return: The result.
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo.
 *
 * @a: the fisrt agrument.
 * @b: the second argument.
 *
 * Return: the result
 */

int op_mod(int a, int b)
{
	return (a % b);
}
